def log_conversation(user_input, bot_response):
    """Log every conversation to a file."""
    log_entry = {
        "timestamp": datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "user": user_input,
        "bot": bot_response
    }

    if not os.path.exists(LOG_FILE):
        with open(LOG_FILE, "w") as f:
            json.dump([], f, indent=4)

    with open(LOG_FILE, "r+") as f:
        data = json.load(f)
        data.append(log_entry)
        f.seek(0)
        json.dump(data, f, indent=4)


def get_gemini_response(user_input):
    """Ask Gemini with full chat history for context memory."""
    global chat_history
    try:
        model = genai.GenerativeModel("gemini-1.5-flash")

        # add user input to history
        chat_history.append({"role": "user", "parts": [user_input]})

        # generate with full conversation
        response = model.generate_content(chat_history)

        bot_reply = response.text.strip() if response and response.text else "I couldn‚Äôt generate a response."

        # add bot reply to history
        chat_history.append({"role": "model", "parts": [bot_reply]})

        return bot_reply

    except Exception as e:
        return f"‚ö†Ô∏è Error fetching Gemini response: {str(e)}"


def generate_response(user_input):
    """Main response generator."""
    lower_input = user_input.lower()

    # Special: Time/Date
    if "time" in lower_input:
        return f"‚è∞ The current time is {datetime.datetime.now().strftime('%H:%M:%S')}."
    if "date" in lower_input:
        return f"üìÖ Today's date is {datetime.datetime.now().strftime('%Y-%m-%d')}."

    # Check static dictionary
    for key in faq_data:
        if key in lower_input:
            return faq_data[key]

    # Gemini fallback
    return get_gemini_response(user_input)


